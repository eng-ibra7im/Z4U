        let sidebarItems = document.querySelectorAll(".asidebar .card");

        // احصل على عنوان الصفحة الحالية
        let currentHeading = document.querySelector(".heading").innerText.trim();

        // التحقق من تطابق العنوان مع عنصر القائمة الجانبية
